class BoughtItem {
  constructor(id, title, description, imgSrc, score, quantity, timeStamp) {
    this.id = id;
    this.title = title;
    this.description = description;
    this.imgSrc = imgSrc;
    this.score = score;
    this.quantity = quantity;
    this.timeStamp = timeStamp;
  }
}

export default BoughtItem;
